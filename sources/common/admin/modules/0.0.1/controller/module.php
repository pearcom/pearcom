class modules_controller_module extends modules_controller_abstract
{

    public function indexAction()
    {

        if(klug_auth::getInstance()->hasIdentity())
        {
            $id = $this->getParam("id");
            $model = $this->getEntity("module");
            $model->load($id);

            $this->_view->set("name",$id);
            $this->_view->set("type",$model->getData("type"));
            $this->_view->set("version",$model->getData("version"));
            $this->_view->set("entities",array($model->getData("entities")));
            $this->_view->set("entities_edit","/modules/module/entities/id/".$id);


            $this->_view->set("location",$model->getData("local"));
            $this->_view->set("action","/modules/module/index/id/".$id);
        }
    }

    public function serviceAction()
    {
        $id = $this->getParam("id");
        $model = $this->getEntity("module");
        $model->load($id);

        $this->_view->set("stotreid",$model->getService($id)->storeid);
        $this->_view->set("url",$model->getService($id)->servce_url);
        $this->_view->set("encription_key",$model->getService($id)->encription_key);
        $this->_view->set("entities",array($model->getData("entities")));

    }
    public function entitiesAction()
    {
        $id = $this->getParam("id");
        $model = $this->getEntity("module");
        $model->load($id);
        $arr=array();
        foreach($model->getData("entities") as $entity)
        {
          $arr[]=$entity;
        }
        $this->_view->set('back_button',true);
        $this->_view->set('entity',$arr);
    }

    public function tenantsAction()
    {
        $id = $this->getParam("id");
        $model = $this->getEntity("tenant");
        $tenants = $model->loadAll("module = '$id'");
        $user =  $this->getEntity("user");
        $arr=array();
        foreach($user->loadAll() as $us)
        {
            foreach($tenants as $tenant)
            {
                if($us->getData("storeid")==$tenant->getData("storeid"))
                {
                    $ar["id"] =  $us->getData("id");
                    $ar["username"] =  $us->getData("username");
                    $ar["email"] =  $us->getData("email");
                    $ar["checked"] =  1;
                    $ar["unassign"] =  "/modules/module/unassign/id/".$ar["id"];
                    $arr[]=$ar;
                }
            }

        }
        $this->_view->set('tenant',$arr);
        $this->_view->set("action","/modules/module/assign/id/".$id);
    }

    public function unassignAction()
    {
        $id = $this->getParam("id");
        $model = $this->getEntity("tenant");
        $user =  $this->getEntity("user");
        if($this->_request->isPost())
        {
            $user_id = $this->getParam("user");
            $user->load($user_id);
            $model->load();
            $model->delete();
        }
        $this->redirect("/modules");
    }
    public function assignAction()
    {

        $id = $this->getParam("id");
        $model = $this->getEntity("tenant");
        $user =  $this->getEntity("user");
        if($this->_request->isPost())
        {
            $user_id = $this->getParam("user");
            $user->load($user_id);
            $model->load();
            $model->setData("storeid",$user->getData("storeid"));
            $model->setData("module",$id);
            $model->setData("key",$user->getData("key"));
            $model->save();
            $this->redirect("/modules/index/edit/id/".$id);

        }
        $tenants = $model->loadAll("module = '$id'");

        $arr=array();
        foreach($user->loadAll() as $us)
        {
            $diplay = true;
            foreach($tenants as $tenant)
            {
                if($us->getData("storeid")==$tenant->getData("storeid"))
                {
                    $diplay = false;
                }
            }
            if($diplay)
            {
                $ar["id"] =  $us->getData("id");
                $ar["username"] =  $us->getData("username");
                $ar["email"] =  $us->getData("email");
                $ar["checked"] =  0;
                $arr[]=$ar;
            }

        }
        $this->_view->set('users',$arr);

    }
    public function addAction()
    {
        $installer = klug_bus_abstract::getInstaller();
        if($this->_request->isPost())
        {

            $url = $this->getParam("url");
            $storeid = $this->getParam("storeid");
            $key = $this->getParam("key");

            $tenant = $this->getEntity("tenant");
            $tenant->getByStoreId($storeid);

            $installer->install("0.0.1",$url,$storeid,$key);
            $this->setNoRender();
            $this->redirect("/modules/index");
        }
        $this->_view->set("versions",$installer->getVersions($url,$storeid,$key));

    }
    public function installAction()
    {
        $installer = klug_bus_abstract::getInstaller();

    }
    public function localAction()
    {
        if($this->_request->isPost())
        {
            $installer = klug_bus_abstract::getInstaller();
             try {
                $adapter = new Zend_File_Transfer_Adapter_Http();
                $adapter->addValidator('Count', false, array('min' => 1 , 'max' => 1));
                $adapter->addValidator('Size', false, array('min' => 400 , 'max' => 3000000 , 'bytestring' => true));
                $adapter->addValidator('Extension', false, array('extension' => 'zip' , 'case' => true));
                 $adapter->addValidator('NotExists', false, "../temp/");
                $adapter->setDestination("../temp/");
                 if (($adapter->isUploaded($adapter->getFileName(null,null,false)))
                     && ($adapter->isValid($adapter->getFileName(null,null,false)))) {
                     $adapter->receive($adapter->getFileName(null,null,false));
                     $installer->installLocal($adapter->getFileName(null,null,false),$this->getParam("type"));
                     $this->_view->set("message","Extension ".$adapter->getFileName(null,null,false)." installed");
                 }
                 foreach($adapter->getMessages() as $message)
                 {
                     $this->_view->set('error',$message);
                 }
            }catch (Exception $e)
             {
                 $this->_view->set("message",$e->getMessage());
             }
        }

    }
    public function remoteAction()
    {
        $installer = klug_bus_abstract::getInstaller();
        $url = "";
        $storeid = "";
        $key = "";
        $versions=array();
        if($this->_request->isPost())
        {
            $url = $this->getParam("url");
            $storeid = $this->getParam("storeid");
            $key = $this->getParam("key");
            if($this->getParam("version"))
            {
            $tenant = $this->getEntity("tenant");
            $tenant->getByStoreId($storeid);

             $result =  $installer->install($this->getParam("version"),$url,$storeid,$key);
             if($result)
             {
                 $this->_view->set("message","Client installed successfully");
             }else
             {
                 $this->_view->set("error","Installation failed ".$result);
             }

            }
            $versions = $installer->getVersions($url,$storeid,$key);
            if(!$versions)
            {
                $this->_view->set("error","Can't get widget versions, please check url and credentials");
            }
            $this->_view->set("version",$this->getParam("version"));
            $this->_view->set("versions",$versions);

        }
        $this->_view->set("url",$url);
        $this->_view->set("storeid",$storeid);
        $this->_view->set("key",$key);

    }
    public function progressAction()
    {
        $progress =33;
        $this->_view->set("progress", $progress);
    }
    public function syncAction()
    {
        $id = $this->getParam("id");
        $model = $this->getEntity("module");
        $model->load($id);
        $count = 0;
        foreach($model->getData("entities") as $entity)
        {
            $ent = $this->getEntity($entity);
          //  var_dump($ent->getAllAttributes());
            $ent_local = $this->getEntity($entity."_local");
          //  var_dump($ent_local->getAllAttributes()->toArray());

            $remotes = $ent->getSync(array(),$ent_local->getAllAttributes()->toArray());
            $count+=sizeof($remotes);
         //   var_dump($remotes);

            $local =$ent_local->getSync($remotes,$ent->getAllAttributes());
     //       var_dump($local);

            $count+=sizeof($local);
            $remotes = $ent->getSync($local);
      //      var_dump($remotes);
            $ent_local->getSync($remotes);
        }
        $this->_view->set('message',"There was $count records synchronized");
    }


}
